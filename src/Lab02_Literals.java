public class Lab02_Literals {
    public static void main(String[] args) {
        //Integer literal
        int decimal=10;
        int hex = 0x64;
        //Floating-point literals
        double d=3.14;
        float f=3.14789f;
        //Character Literals
        char a='A';
        char linebreak ='\n';
        //String Literals
        String str="hello world!";
        //Boolean literal
        boolean isTrue = true;
        boolean isFalse = false;
        //Null literal
        String nullValue = null;

    }
}
