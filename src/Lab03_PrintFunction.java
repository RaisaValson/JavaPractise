public class Lab03_PrintFunction {
    public static void main(String[] args) {
        String a = "Java";
        //To print sentence in one line
        System.out.print("Hello Raisa");
        System.out.print("...How are you...");
        System.out.print("...I am doing good!");
        //To print sentence with line break
        System.out.println("Glad to hear that");
        System.out.println("you are doing well");
        // To print sentence with values added with the string
        System.out.printf("I am trying hard to learn %s" + " %d"+" percent", a, 100);
    }
}
